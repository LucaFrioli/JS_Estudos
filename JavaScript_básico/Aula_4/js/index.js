console.log(`${avô} nasceu em 63 A.C.`);
console.log(`Perdeu o pai muito novo e ${avô} virou homem da familia`);
console.log(`${avô} então conheceu ${avó} e se trornou muito feliz`);
console.log(`${avô} e ${avó} tiveram seu primeiro filho em 33 A.C. e sua filha dois anos depois`);
